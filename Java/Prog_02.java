//Positive and Negative
public class Prog_02 {
    public static void main(String[] args){
        int n = 3;
        if(n > 0){
            System.out.println(n + " 'Positive'");
        } else if (n < 0) {
            System.out.println(n + " 'Negative");
        }
        else {
            System.out.println(n + " 'Zero'");
        }
    }
}

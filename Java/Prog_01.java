//Even and Odd
public class Prog_01 {
    public static void main(String[] args){
        int n = 5;
        int k = 6;
        n = k;
        if (n % 2 == 0){
            System.out.println(n + " is 'Even' number");
        }
        else{
            System.out.println(n + " is 'Odd' number");
        }
    }
}
